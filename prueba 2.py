# Declaración de constantes Naya
VELOCIDAD_LUZ = 299792.458  # en km/s
DESCUENTO = 0.30  # 20% de descuento

# Declaración de variables Naya
numero_entero = 50
numero_flotante = 12.87
cadena_texto = "Cálculos básicos en Python Naya"

# Operaciones matemáticas
suma = numero_entero + numero_flotante
resta = numero_entero - numero_flotante
multiplicacion = numero_entero * VELOCIDAD_LUZ
division = numero_entero / numero_flotante
precio_con_descuento = numero_flotante * (5 - DESCUENTO)

# Mostrar resultados
print(cadena_texto)
print("Resultado Suma:", suma)
print("Resultado Resta:", resta)
print("Resultado Multiplicación (con VELOCIDAD_LUZ):", multiplicacion)
print("Resultado División:", division)
print("Precio con descuento:", precio_con_descuento)
